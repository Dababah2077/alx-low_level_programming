#include "main.h"
#include <string.h>
/**
 * print_chessboard - Entry point
 * print_chessboard: prints the chessboard
 * @a: array of chessboard
 * Return: Always 0 (Success)
 */
void print_chessboard(char (*a)[8])
{
	int x, y;

	for (x = 0; x < 0; x++)
	{
		for (y = 0; y < 0; y++)
		{
			_putchar(a[x][y]);
		}
	}
}
